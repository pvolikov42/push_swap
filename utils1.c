#include "utils.h"

int	abs(int value)
{
	if (value < 0)
		return (-value);
	else
		return (value);
}

int	sign(int value)
{
	if (value < 0)
		return (-1);
	else
		return (1);
}

int	min(int a, int b)
{
	if (a <= b)
		return (a);
	else
		return (b);
}

int	max(int a, int b)
{
	if (a >= b)
		return (a);
	else
		return (b);
}
